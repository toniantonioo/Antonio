package lab5;

public class Calculator {

    public int calc(int first, int second) {

        int calc = first + second;
        return calc;
    }


    public int calc(int first, int second, int third) {

        int calc = first + second + third;
        return calc;
    }


    public float calc(float first, float second) {

        float calc = first + second;
        return calc;
    }

    public float calc(float first, float second, float third) {

        float calc = first + second + third;
        return calc;
    }


}
